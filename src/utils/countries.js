export function sanitizeCountryName(countryName) {
	return countryName
		.normalize('NFD')
		.replace(/[\u0300-\u036f]/g, '')
		.replace(/[- '()]/g, '')
		.toLowerCase();
}

const codeCountry = countries.reduce((acc, country) => {
	acc[country.code] = country;
	return acc;
}, {});

export function getCountryName(code, lang) {
	const country = codeCountry[code];
	if (!country) return;
	return country?.names[lang] ?? country?.name;
}
